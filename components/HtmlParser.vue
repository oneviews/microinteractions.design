<template>
  <div v-html="content">
  </div>
</template>

<script>
// this component makes site-internal links in markdown work with nuxt-router
// https://github.com/nuxt/nuxtjs.org/blob/master/components/HtmlParser.vue
export default {
  props: ['content'],
  mounted() {
    this.addListeners()
  },
  beforeDestroy() {
    this.removeListeners()
  },
  watch: {
    content: 'contentUpdated',
  },
  methods: {
    navigate(event) {
      const href = event.target.getAttribute('href')
      if (href && href[0] === '/') {
        event.preventDefault()
        this.$router.push(href)
      } else if (href.indexOf("#fn") == -1) { // TODO: improve this XXXX
        // open all external links in new tab
        event.preventDefault()
        return window.open(event.target)
        debugger
      }
    },
    addListeners() {
      this._links = this.$el.getElementsByTagName('a')
      for (let i = 0; i < this._links.length; i++) {
        this._links[i].addEventListener('click', this.navigate, false)
      }
    },
    removeListeners() {
      for (let i = 0; i < this._links.length; i++) {
        this._links[i].removeEventListener('click', this.navigate, false)
      }
      this._links = []
    },
  },
}
</script>
